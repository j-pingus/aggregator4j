package com.github.jpingus;

public class Functions {
    public static Double rate(Integer value) {
        return value * 0.87777;
    }

    /**
     * Divide two integers into a double result
     *
     * @param div
     * @param div2
     * @return
     */
    public static Double divide(Integer div, Integer div2) {
        return div.doubleValue() / div2.doubleValue();
    }

}
